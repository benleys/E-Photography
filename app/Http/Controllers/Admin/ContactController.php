    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function answer($id)
    {
        $messages = Contact::find($id);
        return view('admin.contact.answer', compact('messages'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function answered(Request $request, $id) //UPDATE
    {
        $messages = Contact::find($id);
        $messages->answer = ucfirst($request->input('answer'));
        $messages->update();
        return redirect('/dashboard')->with('status', 'Answer successfully send!');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $messages = Contact::find($id);
        $messages->delete();
        return redirect('/dashboard')->with('status', 'Message deleted successfully!');
    }
}
